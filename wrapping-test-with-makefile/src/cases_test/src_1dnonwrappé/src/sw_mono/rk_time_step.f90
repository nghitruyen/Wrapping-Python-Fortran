!> \file rk_time_step.f90
!! \brief This file includes only rk_time_step (for more detail see rk_time_step routine).


!> Time integregation of the scheme.
!!
!! \details This subroutine done the time integration of scheme. Euler integration (rk2) to have order 2 with MUSCL.
!!
!! \param[inout]  dof  Unknowns of the model.
!! \param[in]  mesh Mesh of the model.
SUBROUTINE rk_time_step( dof , mesh )

   USE m_common
   USE m_mesh
   USE m_time_screen!NOADJ
   USE m_model

   implicit none

!======================================================================================================================!
!  Interface Variables
!======================================================================================================================!

   TYPE( msh ), intent(in   )  ::  mesh
   TYPE( unk ), intent(inout)  ::  dof

!======================================================================================================================!
!  Local Variables
!======================================================================================================================!

   
   real(rp), dimension(mesh%ncs+4) :: h_temp 
   real(rp), dimension(mesh%ncs+4) :: q_temp 
   real(rp), dimension(mesh%ncs+4) :: s_temp,u,u_temp,u_temp2


!======================================================================================================================!
!  Begin Subroutine
!======================================================================================================================!

   h_temp( 1 : mesh%ncs+4 )  =  dof%h( 1 : mesh%ncs+4 )
   s_temp( 1 : mesh%ncs+4 )  =  dof%s( 1 : mesh%ncs+4 )
   q_temp( 1 : mesh%ncs+4 )  =  dof%q( 1 : mesh%ncs+4 )

   u_temp( 1 : mesh%ncs+4 )  =  dof%q( 1 : mesh%ncs+4 )/dof%s( 1 : mesh%ncs+4 )
   select case( spatial_scheme )
      
      case('rk2_b1')!NOADJ
         call euler_time_step_first_b1( dof , mesh )!NOADJ
         call euler_time_step_first_b1( dof , mesh )!NOADJ

      case('rk2_b2')!NOADJ
         call euler_time_step_first_b2( dof , mesh )!NOADJ
         call euler_time_step_first_b2( dof , mesh )!NOADJ

      case('rk2_implicit_low_froude')!NOADJ
         call implicit_low_froude( dof , mesh )     !NOADJ           
         call implicit_low_froude( dof , mesh )     !NOADJ           

      case('rk2_explicit_low_froude_b1')!NOADJ
         call explicit_low_froude_b1( dof , mesh )!NOADJ  
         call explicit_low_froude_b1( dof , mesh )!NOADJ  

      case('rk2_explicit_low_froude_b2')  
         call explicit_low_froude_b2( dof , mesh ) 
         call explicit_low_froude_b2( dof , mesh )  

      case default!NOADJ
         call euler_time_step_first( dof , mesh )!NOADJ
         call euler_time_step_first( dof , mesh )!NOADJ  

   end select


   dof%h( 1 : mesh%ncs+4 )  =  0.5_rp * ( h_temp( 1 : mesh%ncs+4 ) + dof%h( 1 : mesh%ncs+4 ) )
   dof%s( 1 : mesh%ncs+4 )  =  0.5_rp * ( s_temp( 1 : mesh%ncs+4 ) + dof%s( 1 : mesh%ncs+4 ) )
   dof%q( 1 : mesh%ncs+4 )  =  0.5_rp * ( q_temp( 1 : mesh%ncs+4 ) + dof%q( 1 : mesh%ncs+4 ) )

   return

END SUBROUTINE rk_time_step
