!> Module m_random.
!!
!! \details This module includes only the defintion of the subroutine init_random_seed. 
MODULE m_random

   USE m_common

   implicit none


CONTAINS

   !> Initialize the random function.
   !!
   !! \details This function initializes the random function.

   SUBROUTINE init_random_seed

      implicit none

      integer(ip)  ::  clock , iseed , nseed

      integer(ip), dimension(:), allocatable  ::  seed
       
      call random_seed( size = nseed )

      allocate( seed( nseed ) )

      call system_clock( count = clock )
       
      seed  =  clock + 37 * (/ ( iseed - 1 , iseed = 1 , nseed ) /)

      call random_seed( put = seed )
       
      deallocate( seed )

      return

   END SUBROUTINE init_random_seed


END MODULE m_random
